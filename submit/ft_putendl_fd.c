#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	n;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	n = '\n';
	write(fd, &n, 1);
}
